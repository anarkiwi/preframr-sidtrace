"""Smoke + structural tests for the sidtrace tool.

Runs sidtrace on a tiny, self-contained PSID (generated by make_test_sid.py,
no HVSC / no network) and asserts both output artifacts are structurally valid:

  - .sidwr.bin and .distill.bin exist; sidwr has whole 12-byte records
  - sidwr cycles are monotonically non-decreasing (PHI1-stamped)
  - every SID write lands in $D400-$D418 with reg == addr & 0x1F, reg <= 24
  - the SDST distill artifact parses, carries a sane access-type map, a song-data
    snapshot, and the PC-tagged SID-write summary (and stays a few KB)
  - meta counts agree with the files

The cycle-by-cycle bus trace is RETIRED -- sidtrace distills the run in the
emulator and emits the compact SDST artifact instead (no multi-GB stream).

The sidtrace binary path is taken from the SIDTRACE_BIN env var (set by the
Makefile / CI) or defaults to the in-repo build location.
"""

import os
import subprocess
import sys
from pathlib import Path

import numpy as np
import pytest

HERE = Path(__file__).resolve().parent
ROOT = HERE.parent
sys.path.insert(0, str(HERE))

from sidtrace_records import (  # noqa: E402
    ACC_EXEC_PLAY,
    ACC_READ_PLAY,
    ACC_WRITE_PLAY,
    ALU_ADC,
    ALU_INC,
    ALU_SBC,
    LK_IMMEDIATE,
    LK_STATE_CELL,
    SIDWR_DT,
    load_sidwr,
    parse_meta,
    parse_sdst,
)

NFRAMES = 50


def _sidtrace_bin():
    cand = os.environ.get("SIDTRACE_BIN") or str(ROOT / "build" / "sidtrace")
    p = Path(cand)
    if not p.exists():
        pytest.skip(f"sidtrace binary not built at {p} (run `make`)")
    return str(p)


